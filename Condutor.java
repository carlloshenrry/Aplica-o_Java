public class Condutor {
    public String nomeCondutor;
    public int idadeCondutor;
    public String nacionalidadeCondutor;
    public String tipoCondutor;
    public int nivelHabilidade;

    public Condutor() {
    }

    public String getNomeCondutor() {
        return nomeCondutor;
    }

    public void setNomeCondutor(String nomeCondutor) {
        this.nomeCondutor = nomeCondutor;
    }

    public int getIdadeCondutor() {
        return idadeCondutor;
    }

    public void setIdadeCondutor(int idadeCondutor) {
        this.idadeCondutor = idadeCondutor;
    }

    public String getNacionalidadeCondutor() {
        return nacionalidadeCondutor;
    }

    public void setNacionalidadeCondutor(String nacionalidadeCondutor) {
        this.nacionalidadeCondutor = nacionalidadeCondutor;
    }

    public String getTipoCondutor() {
        return tipoCondutor;
    }

    public void setTipoCondutor(String tipoCondutor) {
        this.tipoCondutor = tipoCondutor;
    }

    public int getNivelHabilidade() {
        return nivelHabilidade;
    }

    public void setNivelHabilidade(int nivelHabilidade) {
        this.nivelHabilidade = nivelHabilidade;
    }
    
}
